	<h5>Шифон трёхслойный</h5>
	<p>Шифон - (франц . chiffon, букв. - тряпка), тонкая шелковистая хлопчатоб умажная ткань полотняного переплетения повышенной плотности.</p>
	<br>
	<h5>Кашемир</h5>
	<p>Кашеми́р — пух (подшёрсток) горной козы, обитающей в регионах Индии, Китая, Монголии, Пакистана. Изделия из кашемира известны также под названием пашмина (pashmina), они характеризуются легкостью, низкой аллергенностью, очень приятны на ощупь. Пух выщипывают или вычесывают вручную весной, во время линьки коз.</p>
	<br>
	<h5>Пашмина</h5>
	<p>Название «пашмина» произошло от слова «pashm» - так персы называли шерсть. Платки из пашмины ткутся вручную из шерсти коз, которых разводят в Кашмире, одном из штатов Индии. У платков из пашмины очень много достоинств: такие платки отличаются мягкостью и легкостью. Платок из пашмины уже давно стал не только удобным средством защиты от холода, но и модным аксессуаром - платок, украшенный тонкой цветной вышивкой и цветным орнаментом, символизирует настоящую роскошь.</p>
	<p>При создании пашмины не используются ни вискоза, ни другие синтетические материалы. Популярным является сочетание пашмины с волокнами шелка, которые придают платку элегантный блеск. Как правило, соотношение пашмины и шелка в таких платках составляет 70 и 30%. Такие шали отличаются благородным матовым блеском, который дает шелк.</p>
	<p>Чем выше содержание шерсти, тем более дорогим считается платок из пашмины.</p>
	<br>
	<h5>Xлопок</h5>
	<p>Хлопок как средневолокнистый, так и тонковолокнистый в зависимости от величины разрывной нагрузки и степени зрелости делится на семь сортов: отборный (0), 1-й, 2-й, 3-й, 4-й, 5-й и 6-й. В свою очередь Хлопок 0—4-го сортов подразделяется на восемь типов в зависимости от его относительной разрывной нагрузки и штапельной длины.</p>
	<br>
	<h5>Полиэстер</h5>
	<p>Полиэстер / Polyester (PES). Полиэстер – синтетическое волокно. Полиэстерные ткани обладают отличными функциональными характеристиками. Это легкие, «дышащие», быстросохнущие, износостойкие ткани, которые прекрасно сохраняют форму, устойчивы к световому и тепловому воздействию и просты в уходе. Из полиэстера можно произвести ткани различных фактур, матовые или блестящие в зависимости от их предназначения. Полиэстер идеально имитирует фактуру натуральных волокон</p>
