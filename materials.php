	<h2>Платки, палантины, шарфы</h2>
	<h5>Шелк</h5>
	<p>Шёлк — мягкая ткань из нитей, добываемых из кокона тутового шелкопряда. Длина шелковой нити достигает 800 - 1000м. Нить имеет треугольное сечение и, подобно призме отражает свет, что вызывает красивое переливание и блеск.</p>
	<p>Толщина шелковой ткани обычно измеряется в "momme" (произносится "мамми"). Это японская единица веса, используемая для измерения и описания шелковых тканей. Чем выше этот показатель, тем тяжелее, плотнее и прочнее натуральный шелк.</p>
	<br>
	<h5>Шёлк представленный в коллекции Sabellino:</h5>
