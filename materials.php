	<h2>Кожа</h2>
	<p>При производстве изделий Sabellino используется кожа высокого качества: свиная, телячья, кожа козленка, оленья. При этом, кожа может быть представлена эконом-вариантом (производство Турции, Китая), а так же премиум-вариантом (производство Италия) Исходя, из выше сказанного в зависимости от вида и возраста животного кожа получается разной толщины и качества, что влияет на цену продукции.</p>
	<br><br>

	<h5>Типы кожи</h5>
	<p><b>Гладкая кожа</b> - самое высококачественное сырье. Лицевая поверхность очень красивая и сохраняет свой первоначальный рисунок; иногда слегка шлифуется. Срок службы и гигиенические свойства изделий из такого сырья высоки.</p>
	<br>
	<p><b>Шлифованная кожа</b> - шлифовка с бахтармянной /изнаночной стороны кожи.</p>
	<br>
	<p><b>Замша</b> - кожа жирового дубления опоек, овчины, козлины, оленя, лося (самая качественная). Считается «моющейся» кожей, так как не боится мыльного раствора, и сохранят все свои свойства. Замша обладает низким густым ворсом, отлично «дышит», очень мягкая, чрезвычайно эластична и красивая, но не износостойкая.</p>
	<br>
	<p><b>Велюр</b> - от французского слова «velours», т.е. «мохнатый» – это кожа хромого дубления, имеющая сырьевые дефекты. Используются шкуры опойка, выростка, хромовой козлины и шевро; при изготовлении велюра свиные хромовые кожи подлежат отделке с лицевой стороны. Велюр, по сравнению с замшей, имеет более мелкий ворс; в процессе носки велюр быстро загрязняется и теряет внешний вид. Его нельзя стирать, а при чистке - начинает лосниться.</p>
	<br>
	<p><b>Нубук</b> - кожа хромого дубления чаще всего светлых тонов с подшлифованной мерейной поверхности. Вырабатывается из опоек, выростка, иногда из полукожника. Нубук обладает очень низким, почти незаметным ворсом, «дышит», при должном уходе – износоустойчив.</p>
	<br>
	<p><b>Лаковые кожи</b> - кожа хромого дубления с зеркально-блестящей поверхностью, полученная нанесением на кожу полиуретановых и масляных лаков. Покрытие может быть однотонным или с применением различной цветовой гаммы; могут использоваться эффекты «хамелеон», «с блестками внутри», «с изломом». Для улучшения лицевой поверхности может применяться нарезка. Для изготовления лаковой кожи используют главным образом сырье шевро и реже свиные кожи, опоек, конину. Главенствующее значение в качестве покрытия лаковой кожи является толщина лакового слоя - она должна быть незначительной (0,06-0,07 мм). При увеличении лакового слоя тягучесть, гибкость и нежность кожи будет потеряна. Лаковая кожа очень красива, но у нее есть и недостатки: более низкая воздухо- и паропроницаемость, мало устойчива к многократным изгибам. При температуре ниже -15С и выше +25С лаковая кожа покрывается трещинами.</p>
	<br>
	<p><b>Кожа олимпик</b> - телячья кожа с блестящим покрытием на лицевой поверхности.</p>
	<br>
	<p><b>Анилиновые кожи</b> - кожи с природным, «родным» лицом, без искусственного тиснения и искусственных акриловых пропиток, окрашенные только анилиновыми красителями. «Анилиновые кожи» на рынке России очень дороги и занимают менее 1% общего удельного веса.</p>
	<br>
	<p><b>Лайка</b> - очень тонкая кожа алюминиевого дубления, производимая из шкур опоек, козлят и ягнят. Обладает большой растяжимостью и эластичностью, красивым эстетическим видом и мягкостью, но боится влаги.</p>
	<br>
	<p><b>Тисненные кожи</b> - это кожи, получающие рисунок (тиснение) «лица» с помощью прессовки плитами с гравировальным рисунком с целью устранения дефектов и изъянов лицевой поверхности кожи. Чем крупнее рисунок и глубина тиснения, тем больше возможность скрыть дефекты. Рисунок может имитировать кожу рептилий (змеи, крокодила), экзотических животных и других изображения. Такие кожи - низкой и средней ценовой категории.</p>
	<br>
	<p><b>Спилок</b> - кожа, получаемая в результате слоения сырья (распил толстых кож). Спилок не имеет «своей» лицевой поверхности. С искусственной мереей и гладким «лицом» изделия из спилка применяют в обувной и мебельной промышленности, со шлифовкой под велюр – в одежном, обувном и галантерейном производстве. Технология изготовления спилков позволяет получить экономичное сырье с неплохими гигиеническими свойствами.</p>
	<br>
	<p><b>Наппа</b> - сверхмягкая, нежная и эластичная кожа, легко принимает необходимую форму. Может быть матовой или слегка глянцевой.</p>
	<br>
	<p><b>Напплак</b> - лакированная наппа.</p>
