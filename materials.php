		<li>Шёлк серии Люкс - мокрый шёлк</li>
		<li>Плотный шёлк - дорогой шёлк, отличается плотностью</li>
		<li>Шёлк средний - шёлк, средней плотности</li>
		<li>Шёлк тонкий - прозрачный шёлк, шёлк-шифон</li>
