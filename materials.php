	<h2>Экокожа</h2>

	<h5>Что это?</h5>
	<p>Экокожа - это не кожзам, не пвх!</p>
	<p>Экокожа - это вид искусственной кожи. Это современный экологичный материал.</p>
	<p>Экокожа на 30% состоит из натуральных волокон кожи и 30% полиуретана. Искусственные кожи изготавливают путем нанесения на тканую основу пленочного покрытия. Для придания эластичности в него вводят добавки. Эти добавки встраиваются в структуру полимера, а также встраиваются добавки - стабилизаторы, которые позволяют коже быть устойчивой к действию солнечных лучей. Так вот эти добавки не очень экологичны. В отличие от ПВХ, полиуретан абсолютно лишен этих добавок.</p>
	<p>Самой главной причиной необыкновенной мягкости и прочности кожи является полиуретановое покрытие. Полиуретан относится к классу полимеров с исключительно высокой износоустойчивостью (самый простой пример обувь на ПУ - подошве). Полиуретан также способен к «самовосстановлению» наружной деформированной полимерной сетки.</p>
	<p>Эти свойства полиуретанов во многом обусловлены тем, что их современные марки имеют в своем составе множество так называемых «функциональных групп», идентичных натуральной коже!</p>
	<br><br>

	<h5>Характеристики экокожи</h5>
	<p>Экокожа - абсолютно не аллергенный продукт. Технология производства экокожи предусматривает образование сквозных пронизывающих пленку микропор, материал (в отличие от ПВХ) еще и «дышит» не хуже обычных тканей, а также впитывает влагу. Так как экокожа пропускает воздух, то в жару она не перегревается, а в мороз не затвердевает. На ощупь она всегда немного теплая. Кстати, благодаря тому, что в состав материала не входят вредные функциональные группы, полиуретан не выделяет никаких вредных веществ. А благодаря своей воздухопроницаемости экокожа совершенно не имеет запаха, и не создает «парникового эффекта», что выгодно отличает ее от того же ПВХ Экокожа была создана как недорогой материал, имитирующий дорогую натуральную кожу. Однако современный уровень развития науки и техники позволил не только придать искусственной коже свойства натуральной, но и во многом превзойти их! Кроме того эко кожа выдерживает низкотемпературный режим до -40 градусов мороза и может использоваться для изделий используемых в северных районах.</p>
